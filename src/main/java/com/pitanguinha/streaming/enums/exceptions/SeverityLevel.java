/**
 * Enum representing the severity levels of exceptions.
 * 
 * <p>
 * This enum is used to categorize exceptions based on their severity,
 * allowing for better handling and logging of errors in the application.
 * </p>
 * 
 * @since 1.0
 */
public enum SeverityLevel {
    LOW,
    MEDIUM,
    HIGH,
    CRITICAL;
}
